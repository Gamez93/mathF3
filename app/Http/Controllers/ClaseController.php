        //dd($idUser);
        $anotaciones = Clase::where('users_id',$idUser)->get();

        if (!($anotaciones->count() >= 1)) {
            $anotaciones = Clase::create([
                'users_id' => $idUser,
                'tema'       => 'Anotacion Inicial',
                'contenido'  => 'Hoja de estudia',
              ]);
        }

        

        //Retorno de la vista
        return view('Clase.index',compact('anotaciones',));
    }

